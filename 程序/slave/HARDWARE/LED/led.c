#include "led.h"
#include "sys.h"

/*******************************************************************************
* 函数名         : Led_ON
* 功能		  	 : 点亮LED
* 输入			 : 无
* 输出			 : 无
*******************************************************************************/
void Led_ON()
{
	led = 1;
}

/*******************************************************************************
* 函数名         : Led_OFF
* 功能		  	 : 关闭LED
* 输入			 : 无
* 输出			 : 无
*******************************************************************************/
void Led_OFF()
{
	led = 0;
}